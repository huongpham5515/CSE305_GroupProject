    public boolean check(LocalDate start, LocalDate end, int num) {
        List<Request> list = listRequest.get(user.getID());
        for (int i = 0; i < num - 1; i++) {

            if (start.equals(list.get(i).getDateFrom()) || start.equals(list.get(i).getDateTo()) || end.equals(list.get(i).getDateTo())
                    || end.equals(list.get(i).getDateFrom())) {

                return false;
            }

        }
        return true;
    }

