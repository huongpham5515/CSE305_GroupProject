    private void applyBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_applyBtnActionPerformed

        int num = Integer.parseInt(txtNumber.getText());
        String startdate = txtstartDateUpdate.getText();
        String enddate = txtendDateUpdate.getText();
        LocalDate start = LocalDate.parse(startdate);
        LocalDate end = LocalDate.parse(enddate);
        String reason = txtreasonUpdate.getText();
        RequestType requestType = null;
        if (!check(start, end, num)) {
            JOptionPane.showMessageDialog(this, "You already have request this day", "Message", JOptionPane.ERROR_MESSAGE);
        } else if (start.isBefore(LocalDate.now()) || startdate.equals(LocalDate.now())) {
            JOptionPane.showMessageDialog(this, "Start date cannot be before today", "Message", JOptionPane.ERROR_MESSAGE);
            txtstartDateUpdate.setText(LocalDate.now().toString());
        } else if (end.isBefore(start)) {
            JOptionPane.showMessageDialog(this, "End date cannot be before the start date", "Message", JOptionPane.ERROR_MESSAGE);
            txtendDateUpdate.setText(txtstartDateUpdate.getText());
        } else if (reason.equals("")) {
            JOptionPane.showMessageDialog(this, "Please type your reason", "Message", JOptionPane.ERROR_MESSAGE);
        } else if (cbOverdutiesUpdate.isSelected() && start.getDayOfWeek() != DayOfWeek.SUNDAY) {
            JOptionPane.showMessageDialog(this, "The date you choose is not sunday, please choose again", "Message", JOptionPane.ERROR_MESSAGE);
        } else if (!cbLeaveUpdate.isSelected() && !cbOverdutiesUpdate.isSelected()) {
            JOptionPane.showMessageDialog(this, "Choose your request type", "Message", JOptionPane.ERROR_MESSAGE);
        } else {
            if (cbLeaveUpdate.isSelected()) {
                requestType = RequestType.LEAVE;

            } else if (cbOverdutiesUpdate.isSelected()) {
                requestType = RequestType.OVERDUTIES;
            }
            Request requestForm = new Request(start, end, requestType, reason);
            try {
                User_Manager.editRequest(num - 1, user.getID(), requestForm);
                JOptionPane.showMessageDialog(this, "Change succesful", "Message", JOptionPane.INFORMATION_MESSAGE);
                this.setVisible(false);
            } catch (FileNotFoundException ex) {
                Logger.getLogger(RequestChange.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_applyBtnActionPerformed

